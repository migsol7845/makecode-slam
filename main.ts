function makeSprite (image2: Image, width: number, height: number, dx: number, dy: number, playerNumber: number, moveSpeed: number, jumpHeight: number) {
    tempSprite = sprites.create(image2, SpriteKind.Player)
    sprites.setDataNumber(tempSprite, "dx", dx)
    sprites.setDataNumber(tempSprite, "dy", dy)
    sprites.setDataNumber(tempSprite, "player", playerNumber)
    sprites.setDataNumber(tempSprite, "invincibleTime", 0)
    sprites.setDataNumber(tempSprite, "moveSpeed", moveSpeed)
    sprites.setDataNumber(tempSprite, "jumpHeight", jumpHeight)
    sprites.setDataNumber(tempSprite, "percentage", 0)
    hitbox = sprites.create(image.create(width, height), SpriteKind.CharacterHitbox)
    hitbox.setFlag(SpriteFlag.Invisible, true)
    tempSprite.setFlag(SpriteFlag.Ghost, true)
    sprites.setDataSprite(tempSprite, "hitbox", hitbox)
    sprites.setDataSprite(hitbox, "character", tempSprite)
    hitbox.image.fill(7)
    hitbox.z = -1
    attackHitbox = sprites.create(image.create(width, height), SpriteKind.AttackHitbox)
    sprites.setDataSprite(attackHitbox, "character", tempSprite)
    sprites.setDataSprite(tempSprite, "attackHitbox", attackHitbox)
    attackHitbox.setFlag(SpriteFlag.Invisible, true)
    return tempSprite
}
