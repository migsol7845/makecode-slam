sprites.onOverlap(SpriteKind.PlayerSelect, SpriteKind.CharacterSelect, function (sprite, otherSprite) {
    if (sprites.readDataNumber(sprite, "player") == 1) {
        if (controller.A.isPressed()) {
            player1Name = sprites.readDataString(otherSprite, "name")
            if (player1NameText) {
                player1NameText.destroy()
            }
            player1NameText = textsprite.create(toUpperCase(player1Name))
            player1NameText.setPosition(40, 105)
            player1NameText.setOutline(2, 2)
        }
    } else {
        if (controller.player2.isPressed(ControllerButton.A)) {
            player2Name = sprites.readDataString(otherSprite, "name")
            if (player2NameText) {
                player2NameText.destroy()
            }
            player2NameText = textsprite.create(toUpperCase(player2Name))
            player2NameText.setPosition(120, 105)
            player2NameText.setOutline(2, 8)
        }
    }
})
function spawnCharacter (character: string, playerNumber: number) {
    if (character == "strawbob") {
        tempSprite = makeStrawbob(playerNumber)
    } else if (character == "captain cat") {
        tempSprite = makeCaptainCat(playerNumber)
    } else if (character == "glitch") {
        tempSprite = makeGlitch(playerNumber)
    } else {
        tempSprite = makeGideon(playerNumber)
    }
    if (sprites.readDataNumber(tempSprite, "player") == 1) {
        tiles.placeOnRandomTile(sprites.readDataSprite(tempSprite, "hitbox"), assets.tile`myTile2`)
        player1 = tempSprite
    } else {
        tiles.placeOnRandomTile(sprites.readDataSprite(tempSprite, "hitbox"), assets.tile`myTile3`)
        player2 = tempSprite
    }
    updatePercents()
    enableGravity(tempSprite, 500)
}
function doAJump (character: Sprite) {
    if (spriteutils.isDestroyed(character) || areControlsLocked(character)) {
        return
    }
    if (sprites.readDataSprite(character, "hitbox").isHittingTile(CollisionDirection.Bottom)) {
        sprites.readDataSprite(character, "hitbox").vy = 0 - Math.sqrt(2 * (gravity * sprites.readDataNumber(character, "jumpHeight")))
    }
}
