function toUpperCase (text: string) {
    tempString = ""
    for (let index = 0; index <= text.length - 1; index++) {
        if ("abcdefghijklmnopqrstuvwxyz".indexOf(text.charAt(index)) == -1) {
            tempString = "" + tempString + text.charAt(index)
        } else {
            tempString = "" + tempString + String.fromCharCode(65 + "abcdefghijklmnopqrstuvwxyz".indexOf(text.charAt(index)))
        }
    }
    return tempString
}
