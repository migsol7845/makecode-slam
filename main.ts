scene.onOverlapTile(SpriteKind.CharacterHitbox, assets.tile`myTile1`, function (sprite, location) {
    if (!(spriteutils.isDestroyed(sprite))) {
        sprite.destroy()
        sprites.readDataSprite(sprite, "character").destroy()
        sprites.readDataSprite(sprites.readDataSprite(sprite, "character"), "attackHitbox").destroy()
        if (sprites.readDataNumber(sprites.readDataSprite(sprite, "character"), "player") == 1) {
            player1Stock += -1
            if (player1Stock == 0) {
                endRound(2)
            }
        } else {
            player2Stock += -1
            if (player2Stock == 0) {
                endRound(1)
            }
        }
        scene.cameraShake(4, 1000)
        timer.after(2000, function () {
            spawnCharacter(sprites.readDataString(sprites.readDataSprite(sprite, "character"), "name"), sprites.readDataNumber(sprites.readDataSprite(sprite, "character"), "player"))
        })
    }
})
function updatePercents () {
    player1PercentText.setText("" + Math.round(sprites.readDataNumber(player1, "percentage")) + "%")
    player1PercentText.x = 32
    player1PercentText.y = 115
    player2PercentText.setText("" + Math.round(sprites.readDataNumber(player2, "percentage")) + "%")
    player2PercentText.x = 128
    player2PercentText.y = 115
}
