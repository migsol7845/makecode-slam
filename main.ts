function slam (slammer: Sprite, slamee: Sprite) {
    if (sprites.readDataNumber(slamee, "invincibleTime") < game.runtime()) {
        if (slammer != slamee && sprites.readDataSprite(slammer, "proxySlammer") != slamee) {
            sprites.changeDataNumberBy(slamee, "percentage", sprites.readDataNumber(slammer, "slamStrength"))
            slameePercent = sprites.readDataNumber(slamee, "percentage")
            if (sprites.readDataNumber(slammer, "slamCooldown") > 0 || sprites.readDataNumber(slammer, "slamCooldown") < 0) {
                sprites.setDataNumber(slamee, "invincibleTime", game.runtime() + sprites.readDataNumber(slammer, "slamCooldown"))
            } else {
                sprites.setDataNumber(slamee, "invincibleTime", game.runtime() + 100)
            }
            if (sprites.readDataNumber(slammer, "slamStrength") >= 10 && slameePercent >= 25) {
                setControlsOn(slamee, false)
                spriteutils.setVelocityAtAngle(sprites.readDataSprite(slamee, "hitbox"), Math.atan2(slamee.y - (slammer.y + 5), slamee.x - slammer.x), slameePercent * 2)
                characterAnimations.setCharacterAnimationsEnabled(slamee, false)
                sprites.setDataNumber(slamee, "controlLockTime", game.runtime() + Math.min(slameePercent, 100) / 100 * 1500)
                sprites.readDataSprite(slamee, "hitbox").fx = 25
            }
            updatePercents()
        }
    }
}
